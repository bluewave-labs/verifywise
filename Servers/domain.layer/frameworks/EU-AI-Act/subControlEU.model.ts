import {
  Column,
  DataType,
  ForeignKey,
  Model,
  Table,
} from "sequelize-typescript";
import { ControlStructEUModel } from "./controlStructEU.model";
import { UserModel } from "../../models/user/user.model";
import {
  StatusCompliance,
  STATUSES_COMPLIANCE,
} from "../../../types/status.type";

